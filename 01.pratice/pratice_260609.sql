                           AND M3.ITEM_CD  = M1.ITEM_CD
                        ) AS QTY_IN_BOX
                  FROM (SELECT BRAND_CD, ITEM_CD
                             , SUM(ORDER_QTY) AS SUM_QTY
                          FROM A_OUT_D
                         GROUP BY BRAND_CD, ITEM_CD
                        ) M1
                )
        )
 WHERE RNK < 4
