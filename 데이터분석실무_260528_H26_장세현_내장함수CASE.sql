SELECT CASE WHEN ROWNUM <= 2 THEN ITEM_CD ELSE 'etc' END ITEM
      , SUM(SUM_QTY) AS SUM_QTY
  FROM (
        SELECT ITEM_CD, SUM(ORDER_QTY) AS SUM_QTY
          FROM A_OUT_D
         GROUP BY ITEM_CD
         ORDER BY SUM_QTY DESC
        )
 GROUP BY CASE WHEN ROWNUM <= 2 THEN ROWNUM ELSE 999 END
        , CASE WHEN ROWNUM <= 2 THEN ITEM_CD ELSE 'etc' END 
 ORDER BY CASE WHEN ROWNUM <= 2 THEN ROWNUM ELSE 999 END
;

--<2-2>
SELECT BRAND_CD AS 브랜드
      , CASE 
          WHEN SUM_QTY > 4 THEN '상'
          WHEN SUM_QTY > 2 THEN '중'
          WHEN SUM_QTY > 0 THEN '하'
          END AS GRADE_DIV
       , SUM(SUM_QTY) AS INV_SUM
 FROM (
      SELECT BRAND_CD, INVOICE_NO, SUM(ORDER_QTY) AS SUM_QTY
        FROM A_OUT_D
       GROUP BY BRAND_CD, INVOICE_NO
       )
 GROUP BY BRAND_CD 
        , CASE 
            WHEN SUM_QTY > 4 THEN '상'
            WHEN SUM_QTY > 2 THEN '중'
            WHEN SUM_QTY > 0 THEN '하'
          END
;

--<2-1>
SELECT BRAND_CD AS 브랜드 
      ,CASE SUBSTR(OUT_TYPE_DIV, 1, 2) 
            WHEN 'M1' THEN '상온'
            WHEN 'M2' THEN '저온'
            END AS TEMP_DIV
      ,COUNT(1) AS INV_CNT
  FROM A_OUT_M
 GROUP BY BRAND_CD 
        , CASE SUBSTR(OUT_TYPE_DIV, 1, 2) 
            WHEN 'M1' THEN '상온'
            WHEN 'M2' THEN '저온'
            END
;

--<1-2>
SELECT BRAND_CD, INVOICE_NO, LINE_NO, ITEM_CD, ORDER_QTY
      , CASE 
          WHEN ORDER_QTY > 4 THEN '상'
          WHEN ORDER_QTY > 2 THEN '중'
          WHEN ORDER_QTY > 0 THEN '하'
          END AS GRADE
 FROM A_OUT_D
;

