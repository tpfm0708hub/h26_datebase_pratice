SELECT OUTBOUND_DATE, TO_CHAR(OUTBOUND_DATE, 'DY') AS DY, INVOICE_NO
      ,CASE MOD(TO_NUMBER(SUBSTR(INVOICE_NO, 2)), 2) WHEN 0 THEN '짝수' ELSE '홀수' END AS EVENODD
      ,ORDER_NM
      ,CASE SUBSTR(OUT_TYPE_DIV, 1, 2) 
            WHEN 'M1' THEN '상온'
            WHEN 'M2' THEN '저온'
            END AS TEMP_DIV
  FROM A_OUT_M
;

