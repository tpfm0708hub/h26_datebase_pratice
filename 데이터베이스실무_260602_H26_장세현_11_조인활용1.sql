SELECT TO_CHAR(M3.OUT_DATE, 'YYYY/MM/DD') AS OUT_DATE
     , NVL(M4.QTY, 0) AS QTY
FROM (
       SELECT TRUNC(TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD'), 'MM') + LEVEL - 1 AS OUT_DATE
         FROM DUAL
      CONNECT BY LEVEL <= TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD') - TRUNC(TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD'), 'MM') + 1
      ) M3 LEFT JOIN (
                      SELECT M1.OUTBOUND_DATE AS OUT_DATE, SUM(M2.ORDER_QTY) AS QTY
                        FROM LO_OUT_M M1
                             JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
                       WHERE M1.OUTBOUND_DATE BETWEEN TRUNC(TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD'), 'MM') AND TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD')
                       GROUP BY M1.OUTBOUND_DATE
                      ) M4
            ON M4.OUT_DATE = M3.OUT_DATE
 ORDER BY OUT_DATE
;

