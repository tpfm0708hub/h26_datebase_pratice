--실전문제②
SELECT M1.INVOICE_NO, M1.LINE_NO, M1.ITEM_CD, M1.ITEM_NM, M1.ORDER_QTY, SUM(M2.ORDER_QTY) AS ACC_ORDER_QTY
  FROM LO_OUT_D M1
       JOIN LO_OUT_D M2 ON M2.INVOICE_NO IN ('346724703845', '346724706214')
                       AND M2.INVOICE_NO || LPAD(M2.LINE_NO, 3, '0') <= M1.INVOICE_NO || LPAD(M1.LINE_NO, 3, '0')
 WHERE M1.INVOICE_NO IN ('346724703845', '346724706214')
 GROUP BY M1.INVOICE_NO, M1.LINE_NO, M1.ITEM_CD, M1.ITEM_NM, M1.ORDER_QTY
 ORDER BY M1.INVOICE_NO, M1.LINE_NO
;

WITH MY_SET
AS(
  SELECT INVOICE_NO, LINE_NO, ITEM_CD, ORDER_QTY
       , ROW_NUMBER() OVER(ORDER BY INVOICE_NO, LINE_NO) AS RNUM
    FROM LO_OUT_D
   WHERE INVOICE_NO IN ('346724703845', '346724706214')
  )
SELECT M1.INVOICE_NO, M1.LINE_NO, M1.ITEM_CD, M1.ORDER_QTY, SUM(M2.ORDER_QTY) AS ACC_ORDER_QTY
  FROM MY_SET M1
       JOIN MY_SET M2 ON M2.RNUM <= M1.RNUM
 GROUP BY M1.INVOICE_NO, M1.LINE_NO, M1.ITEM_CD, M1.ORDER_QTY
 ORDER BY M1.INVOICE_NO, M1.LINE_NO
;

--실전문제①
SELECT M1.INVOICE_NO, M1.LINE_NO, M1.ITEM_CD, M1.ORDER_QTY, SUM(M2.ORDER_QTY) AS ACC_ORDER_QTY
  FROM LO_OUT_D M1
       JOIN LO_OUT_D M2 ON  M2.INVOICE_NO   = M1.INVOICE_NO
                        AND M2.LINE_NO     <= M1.LINE_NO
 WHERE M1.INVOICE_NO IN ('346724703845', '346724706214')
 GROUP BY M1.INVOICE_NO, M1.LINE_NO, M1.ITEM_CD, M1.ORDER_QTY
 ORDER BY M1.INVOICE_NO, M1.LINE_NO
;
