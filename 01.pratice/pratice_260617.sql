          FROM DUAL
          CONNECT BY LEVEL <= TO_DATE(:OUTBOUND_DATE ,'YYYY-MM-DD') - ADD_MONTHS(TO_DATE(:OUTBOUND_DATE ,'YYYY-MM-DD'), -1)
        ) T1 LEFT JOIN (SELECT M1.OUTBOUND_DATE, SUM(M2.ORDER_QTY) AS ORDER_QTY
