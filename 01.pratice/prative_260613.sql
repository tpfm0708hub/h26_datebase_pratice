SELECT *
  FROM (SELECT M1.INVOICE_NO, M1.OUTBOUND_NO, M1.OUTBOUND_BATCH, M2.ITEM_CD, M2.ITEM_NM, M2.ORDER_QTY
          FROM LO_OUT_M M1 JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
         WHERE M1.OUTBOUND_DATE BETWEEN '2019-09-03' AND '2019-09-10'
           AND M2.ITEM_NM LIKE '%골뱅이%'
         ORDER BY ORDER_QTY DESC
        )
 WHERE ROWNUM <= 5
;

