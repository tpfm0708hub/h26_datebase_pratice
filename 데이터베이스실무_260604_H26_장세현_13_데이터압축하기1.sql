--실전문제④
WITH BASE AS (
SELECT M2.ITEM_CD, M2.ITEM_NM
     , SUM(M2.ORDER_QTY) AS ORDER_QTY
  FROM LO_OUT_M M1
       JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
 WHERE M1.OUTBOUND_DATE BETWEEN TO_DATE('2019-06-01', 'YYYY-MM-DD')
                            AND TO_DATE('2019-06-30', 'YYYY-MM-DD')
   AND M2.ITEM_NM LIKE '%참치%'
 GROUP BY M2.ITEM_CD, M2.ITEM_NM
 ),
BASE_01 AS (
SELECT ITEM_CD, ITEM_NM, ORDER_QTY
     , ROW_NUMBER() OVER(ORDER BY ORDER_QTY DESC) AS RNUM
  FROM BASE
 ORDER BY ORDER_QTY DESC
 )

SELECT RNUM, ITEM_CD, ITEM_NM, ORDER_QTY
  FROM BASE_01
 WHERE RNUM <= 5

UNION ALL

SELECT 0              AS RNUM
     , '99999'        AS ITEM_CD
     , 'TOP5제외 합계' AS ITEM_NM
     , SUM(ORDER_QTY) AS ORDER_QTY
  FROM BASE_01
 WHERE RNUM > 5
;

