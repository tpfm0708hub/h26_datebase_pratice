  FROM A_OUT_M
WHERE BRAND_CD          = '1001'
  AND OUT_TYPE_DIV    IN ('M11', 'M12')
  AND OUTBOUND_DATE   BETWEEN TO_DATE('20230104', 'YYYYMMDD') AND
                              TO_DATE('20230105', 'YYYYMMDD')
  AND INVOICE_NO      LIKE '#0%'
ORDER BY OUT_TYPE_DIV ASC, OUTBOUND_DATE DESC
;

