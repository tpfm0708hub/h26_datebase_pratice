--실전문제②
SELECT SUM(CASE WHEN TO_CHAR(OUTBOUND_DATE, 'D') = '1' THEN ORDER_QTY END) AS SUN
     , SUM(CASE WHEN TO_CHAR(OUTBOUND_DATE, 'D') = '2' THEN ORDER_QTY END) AS MON
     , SUM(CASE WHEN TO_CHAR(OUTBOUND_DATE, 'D') = '3' THEN ORDER_QTY END) AS TUE
     , SUM(CASE WHEN TO_CHAR(OUTBOUND_DATE, 'D') = '4' THEN ORDER_QTY END) AS WED
     , SUM(CASE WHEN TO_CHAR(OUTBOUND_DATE, 'D') = '5' THEN ORDER_QTY END) AS THU
     , SUM(CASE WHEN TO_CHAR(OUTBOUND_DATE, 'D') = '6' THEN ORDER_QTY END) AS FRI
     , SUM(CASE WHEN TO_CHAR(OUTBOUND_DATE, 'D') = '7' THEN ORDER_QTY END) AS SAT
  FROM LO_OUT_M M1
       JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
 WHERE OUTBOUND_DATE BETWEEN TO_DATE('201901', 'YYYY-MM')
                         AND LAST_DAY(TO_DATE('201912', 'YYYY-MM'))
;

SELECT TO_CHAR(OUTBOUND_DATE, 'DAY') AS DAY
     , SUM(ORDER_QTY) AS ORDER_QTY
  FROM LO_OUT_M M1
       JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
 WHERE OUTBOUND_DATE BETWEEN TO_DATE('201909', 'YYYY-MM')
                         AND LAST_DAY(TO_DATE('201909', 'YYYY-MM'))
 GROUP BY TO_CHAR(OUTBOUND_DATE, 'DAY')
        , TO_CHAR(OUTBOUND_DATE, 'D')
 ORDER BY TO_CHAR(OUTBOUND_DATE, 'D')
;

--실전문제①
SELECT TO_CHAR(OUTBOUND_DATE, 'YYYY/MM/DD') AS OUTBOUND_DATE
     , SUM(ORDER_QTY) AS SUM_QTY
  FROM LO_OUT_M M1
       JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
 WHERE OUTBOUND_DATE BETWEEN TO_DATE('201909', 'YYYY-MM')
                         AND LAST_DAY(TO_DATE('201909', 'YYYY-MM'))
 GROUP BY OUTBOUND_DATE
 ORDER BY SUM_QTY DESC
;
