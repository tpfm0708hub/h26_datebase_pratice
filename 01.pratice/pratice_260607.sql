SELECT INVOICE_NO
     , OUTBOUND_DATE
     , TO_CHAR(OUTBOUND_DATE, 'DAY') AS DAYY
     , OUTBOUND_NO
  FROM LO_OUT_M
 WHERE INVOICE_NO IN ('346724706214', '346724793596', '346724869970')
;

--실전문제②
SELECT NVL(TO_CHAR(SUM(ORDER_QTY)), 'Empty..') AS ORDER_QTY
  FROM LO_OUT_D
 WHERE INVOICE_NO = '346724706215'
;

--내장함수CASE
--실전문제①
SELECT OUTBOUND_DATE
     , COUNT(1) AS INVOICE_CNT
     , COUNT(CASE WHEN OUT_TYPE_DIV LIKE 'M1%' THEN 1 END) M1_CNT
     , COUNT(CASE WHEN OUT_TYPE_DIV LIKE 'M2%' THEN 1 END) M2_CNT
  FROM LO_OUT_M
 WHERE OUTBOUND_DATE BETWEEN TO_DATE(SUBSTR(:OUTBOUND_DATE, 1, 7) || '01', 'YYYY-MM-DD')
                         AND TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD')
   AND OUT_BOX_DIV LIKE CASE :COND WHEN '1' THEN 'D'
                                   WHEN '2' THEN 'F'
                                   WHEN '0' THEN NULL
                                   END || '%'
 GROUP BY OUTBOUND_DATE
;

--실전문제②
SELECT ITEM_BAR_CD
     , COUNT(DISTINCT ITEM_CD) AS ITEM_CNT 
  FROM LO_OUT_D
 GROUP BY ITEM_BAR_CD
HAVING COUNT(DISTINCT ITEM_CD) >= 2
 ORDER BY CASE WHEN ITEM_BAR_CD IS NULL THEN 2 ELSE 1 END
        , ITEM_CNT DESC
;