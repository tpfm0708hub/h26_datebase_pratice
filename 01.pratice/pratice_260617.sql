                      WHERE M1.OUTBOUND_DATE BETWEEN ADD_MONTHS(TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD'), -1)
                                                 AND TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD')
                      GROUP BY M1.OUTBOUND_DATE
                      ) T2 ON T2.OUTBOUND_DATE = T1.OUT_DATE
 WHERE T2.ORDER_QTY IS NULL
