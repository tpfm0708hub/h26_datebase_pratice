--이전 복습: 실전문제②-스칼라 쿼리
SELECT INVOICE_NO
     , OUT_TYPE_DIV
     , OUT_BOX_DIV
     , TO_NUMBER(SUBSTR(MAX_VAL, 1, 5)) AS MAX_ORDER_QTY
     , TO_NUMBER(SUBSTR(MAX_VAL, 7, 9)) AS MAX_LINE_NO
  FROM (
        SELECT M1.INVOICE_NO
             , M1.OUT_TYPE_DIV
             , M1.OUT_BOX_DIV
             , (SELECT MAX(LPAD(M2.ORDER_QTY, 5, '0') || '-' || LPAD(M2.LINE_NO, 3, '0')) 
                  FROM LO_OUT_D M2
                 WHERE M2.INVOICE_NO = M1.INVOICE_NO) AS MAX_VAL
          FROM LO_OUT_M M1
         WHERE M1.OUTBOUND_DATE = '2019-06-03'
           AND OUTBOUND_NO      BETWEEN 'D190603-897353'
                                    AND 'D190603-897360'
        )
;

