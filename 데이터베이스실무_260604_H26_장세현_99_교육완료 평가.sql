SELECT CASE :SCHOOL WHEN '1' THEN '초등학교 ' WHEN '2' THEN '중학교 ' WHEN '3' THEN '고등학교 ' END || TO_CHAR(NO) || '학년' AS SCHOOL 
     , TO_NUMBER(SUBSTR(:BIRTHDAY, 1, 4)) + CASE :SCHOOL WHEN '1' THEN  6 + NO WHEN '2' THEN  6 + 6 + NO WHEN '3' THEN  6 + 6 + 3 + NO END AS YEAR
  FROM CS_NO
 WHERE NO <= CASE :SCHOOL WHEN '1' THEN 6 WHEN '2' THEN 3 WHEN '3' THEN 3 END
;

--오답
WITH ELEMENT_SCHOOL AS (
SELECT '초등학교 ' || LEVEL || '학년' AS SCHOOL
     , TO_NUMBER(TO_CHAR(TO_DATE(:BIRTHDAY, 'YYYY-MM-DD'), 'YYYY')) + LEVEL + 6 AS YEAR
  FROM DUAL
CONNECT BY LEVEL <= 6
),
MIDDLE_SCHOOL AS (
SELECT '중학교 ' || LEVEL || '학년' AS SCHOOL
     , TO_NUMBER(TO_CHAR(TO_DATE(:BIRTHDAY, 'YYYY-MM-DD'), 'YYYY')) + LEVEL + 12 AS YEAR
  FROM DUAL
CONNECT BY LEVEL <= 3
),
HIGH_SCHOOL AS (
SELECT '고등학교 ' || LEVEL || '학년' AS SCHOOL
     , TO_NUMBER(TO_CHAR(TO_DATE(:BIRTHDAY, 'YYYY-MM-DD'), 'YYYY')) + LEVEL + 15 AS YEAR
  FROM DUAL
CONNECT BY LEVEL <= 3
),
SCHOOL_TAB AS (
SELECT *
  FROM ELEMENT_SCHOOL
UNION ALL
SELECT *
  FROM MIDDLE_SCHOOL
UNION ALL
SELECT *
  FROM HIGH_SCHOOL
)

SELECT *
  FROM SCHOOL_TAB
 WHERE CASE WHEN :SCHOOL = '3' THEN ROWNUM < 13
            WHEN :SCHOOL = '2' THEN ROWNUM < 10
            WHEN :SCHOOL = '1' THEN ROWNUM < 7  
 END
;