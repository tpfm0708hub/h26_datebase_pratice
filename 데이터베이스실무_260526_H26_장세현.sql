SELECT ROWNUM AS RNUM
      ,OUTBOUND_DATE
      ,INV_CNT
  FROM (
        SELECT OUTBOUND_DATE, COUNT(*) AS INV_CNT
          FROM LO_OUT_M
         WHERE OUTBOUND_DATE BETWEEN :OUTBOUND_DATE1 AND :OUTBOUND_DATE2
           --AND ROWNUM <= 3
         GROUP BY OUTBOUND_DATE
         ORDER BY INV_CNT DESC
        )
WHERE ROWNUM <= 3
;

