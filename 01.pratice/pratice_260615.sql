SELECT CASE WHEN ROWNUM <= 5 THEN ROWNUM ELSE 0 END               AS RNUM
     , CASE WHEN ROWNUM <= 5 THEN ITEM_CD ELSE '99999' END          AS ITEM_CD
     , CASE WHEN ROWNUM <= 5 THEN ITEM_NM ELSE 'TOP5제외 합계' END AS ITEM_NM
     , SUM(ORDER_QTY) AS ORDER_QTY
  FROM (SELECT M2.ITEM_CD, M2.ITEM_NM, SUM(M2.ORDER_QTY) AS ORDER_QTY
          FROM LO_OUT_M M1 JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
         WHERE M1.OUTBOUND_DATE BETWEEN '2019-06-01' AND '2019-06-30'
           AND M2.ITEM_NM       LIKE     '%참치%'
         GROUP BY M2.ITEM_CD, M2.ITEM_NM
         ORDER BY SUM(M2.ORDER_QTY) DESC
        )
 GROUP BY CASE WHEN ROWNUM <= 5 THEN ROWNUM ELSE 0 END
        , CASE WHEN ROWNUM <= 5 THEN ITEM_CD ELSE '99999' END          
        , CASE WHEN ROWNUM <= 5 THEN ITEM_NM ELSE 'TOP5제외 합계' END 
 ORDER BY CASE WHEN RNUM = 0 THEN 99999 ELSE RNUM END
;

WITH BASE_01 AS (
SELECT M2.ITEM_CD, M2.ITEM_NM, SUM(M2.ORDER_QTY) AS ORDER_QTY
          FROM LO_OUT_M M1 JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
         WHERE M1.OUTBOUND_DATE BETWEEN '2019-06-01' AND '2019-06-30'
           AND M2.ITEM_NM       LIKE     '%참치%'
         GROUP BY M2.ITEM_CD, M2.ITEM_NM
         ORDER BY SUM(M2.ORDER_QTY) DESC
),
     BASE_02 AS (
SELECT ITEM_CD, ITEM_NM, ORDER_QTY
     , ROW_NUMBER() OVER(ORDER BY ORDER_QTY DESC) AS RNUM
  FROM BASE_01
 ORDER BY ORDER_QTY DESC
)

SELECT RNUM, ITEM_CD, ITEM_NM, ORDER_QTY
  FROM BASE_02
 WHERE RNUM <= 5

UNION ALL

SELECT 0               AS RNUM
     , '99999'         AS ITEM_CD
     , 'TOP5제외 합계'  AS ITEM_NM
     , SUM(ORDER_QTY)  AS ORDER_QTY
  FROM BASE_02
 WHERE RNUM >= 5
;
