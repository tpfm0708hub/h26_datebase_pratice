SELECT LAG(NULL, 1, OUTBOUND_DATE) OVER(PARTITION BY OUTBOUND_DATE ORDER BY RNK) AS OUTBOUND_DATE
     , RNK, ITEM_NM, ORDER_QTY
  FROM (SELECT M1.OUTBOUND_DATE, M2.ITEM_NM, SUM(M2.ORDER_QTY) AS ORDER_QTY
             , ROW_NUMBER() OVER(PARTITION BY M1.OUTBOUND_DATE ORDER BY SUM(M2.ORDER_QTY) DESC) AS RNK
          FROM LO_OUT_M M1 JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
         WHERE M1.OUTBOUND_DATE BETWEEN '2019-09-03' AND '2019-09-05'
         GROUP BY M1.OUTBOUND_DATE, M2.ITEM_NM
        )
 WHERE RNK <= 3
;

