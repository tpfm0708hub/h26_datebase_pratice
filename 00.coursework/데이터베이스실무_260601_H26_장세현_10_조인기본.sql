SELECT *
  FROM (
        SELECT M3.*
              , TRUNC(SUM_QTY / QTY_IN_BOX) AS BOX_CNT
              , MOD(SUM_QTY, QTY_IN_BOX)    AS ITEM_CNT
          FROM (
                SELECT M1.BRAND_CD, M2.ITEM_CD, C1.ITEM_NM, C1.QTY_IN_BOX, SUM(M2.ORDER_QTY) AS SUM_QTY
                  FROM A_OUT_M M1
                       JOIN A_OUT_D M2 ON  M2.BRAND_CD   = M1.BRAND_CD
                                       AND M2.INVOICE_NO = M1.INVOICE_NO
                       JOIN A_ITEM  C1 ON  C1.BRAND_CD   = M2.BRAND_CD
                                       AND C1.ITEM_CD    = M2.ITEM_CD
                 WHERE OUTBOUND_DATE BETWEEN TO_DATE('2023-01-01', 'YYYY-MM-DD')
                                         AND TO_DATE('2023-01-04', 'YYYY-MM-DD')
                 GROUP BY M1.BRAND_CD, M2.ITEM_CD, C1.ITEM_NM, C1.QTY_IN_BOX
                ) M3
          ORDER BY BOX_CNT DESC
        ) 
 WHERE ROWNUM <= 3
;

