                 WHERE M1.OUTBOUND_DATE BETWEEN '2023-01-01' AND '2023-01-04'
                 GROUP BY M1.BRAND_CD, M2.ITEM_CD
                ) M3 JOIN A_ITEM M4 ON  M4.BRAND_CD  = M3.BRAND_CD
                                    AND M4.ITEM_CD   = M3.ITEM_CD
        ) M4
;

