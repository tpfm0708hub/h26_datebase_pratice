--문제①
WITH BASE_01 AS(
SELECT TO_CHAR(M1.OUTBOUND_DATE, 'DY') AS DY
     , SUM(ORDER_QTY) AS ORDER_QTY
  FROM LO_OUT_M M1
       JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
 WHERE OUTBOUND_DATE BETWEEN ADD_MONTHS(TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD'), -1)
                         AND TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD') 
 GROUP BY TO_CHAR(M1.OUTBOUND_DATE, 'DY')
 ORDER BY ORDER_QTY DESC
),
BASE_02 AS(
SELECT BASE_01.*
      , ROW_NUMBER() OVER(ORDER BY ORDER_QTY DESC) AS RNK
  FROM BASE_01
)

SELECT DY, ORDER_QTY
  FROM BASE_02
 WHERE RNK <= 3

UNION ALL

SELECT 'etc' AS DY
     , SUM(ORDER_QTY) AS ORDER_QTY
  FROM BASE_02
 WHERE RNK > 3
;

