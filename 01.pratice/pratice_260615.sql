SELECT OUTBOUND_DATE, OUTBOUND_BATCH, ORDER_QTY
     , RANK()         OVER(PARTITION BY OUTBOUND_DATE ORDER BY ORDER_QTY DESC) AS RNK_PER_DAY
     , SUM(ORDER_QTY) OVER(PARTITION BY OUTBOUND_DATE ORDER BY ORDER_QTY DESC) AS ACC_SUM_PER_RNK
     , SUM(ORDER_QTY) OVER(PARTITION BY OUTBOUND_DATE)                         AS SUM_PER_DAY
     , COUNT(1)       OVER(PARTITION BY OUTBOUND_DATE)                         AS CNT_PER_DAY
     , COUNT(1)       OVER()                                                   AS CNT_TOT
  FROM (SELECT M1.OUTBOUND_DATE, M1.OUTBOUND_BATCH, SUM(M2.ORDER_QTY) AS ORDER_QTY
          FROM LO_OUT_M M1 JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
         WHERE M1.OUTBOUND_DATE BETWEEN '2019-09-03' AND '2019-09-10'
           AND M1.OUT_TYPE_DIV  = 'M11'
         GROUP BY M1.OUTBOUND_DATE, M1.OUTBOUND_BATCH
         ORDER BY OUTBOUND_DATE
        )
;

