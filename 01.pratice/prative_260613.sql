SELECT M1.INVOICE_NO, M1.OUTBOUND_DATE, M1.OUT_TYPE_DIV, M2.LINE_NO, M2.ITEM_CD, M3.ITEM_NM, M2.ORDER_QTY
     , NVL(C1.CODE_NM, 'Failed..') AS TEMP_NM
     , NVL(C2.CODE_NM, 'Failed..') AS OUT_TYPE_NM
  FROM LO_OUT_M M1 JOIN LO_OUT_D M2     ON  M2.INVOICE_NO   = M1.INVOICE_NO
                   JOIN CM_ITEM  M3     ON  M3.ITEM_CD      = M2.ITEM_CD
                   LEFT JOIN CS_CODE C1 ON  C1.CODE_GRP = 'LDIV011'
                                        AND C1.CODE_CD  = M1.TEMP_DIV
                   LEFT JOIN CS_CODE C2 ON  C2.CODE_GRP = 'LDIV03'
                                        AND C2.CODE_CD  = M1.OUT_TYPE_DIV
 WHERE M1.INVOICE_NO IN ('346724703834', '346724722535', '346724717915')
;

--실전문제③
SELECT M1.OUT_DATE, SUM(M3.ORDER_QTY) AS QTY
  FROM (SELECT TO_DATE(SUBSTR(:OUTBOUND_DATE, 1, 8) || '01', 'YYYY-MM-DD') + LEVEL - 1 AS OUT_DATE
           FROM DUAL
        CONNECT BY LEVEL <= TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD') - TO_DATE(SUBSTR(:OUTBOUND_DATE, 1, 8) || '01', 'YYYY-MM-DD') + 1
        ) M1 LEFT JOIN LO_OUT_M M2 ON M2.OUTBOUND_DATE = M1.OUT_DATE
             LEFT JOIN LO_OUT_D M3 ON M3.INVOICE_NO    = M2.INVOICE_NO
 GROUP BY M1.OUT_DATE
 ORDER BY OUT_DATE
;

