SELECT RNUM, ITEM_CD, ITEM_NM, SUM(ORDER_QTY) AS ORDER_QTY
  FROM (SELECT CASE WHEN RNUM <= 5 THEN RNUM      ELSE 0                END AS RNUM
             , CASE WHEN RNUM <= 5 THEN ITEM_CD   ELSE '99999'          END AS ITEM_CD
             , CASE WHEN RNUM <= 5 THEN ITEM_NM   ELSE 'TOP5제외 합계' END AS ITEM_NM
             , ORDER_QTY
          FROM (SELECT ROW_NUMBER() OVER(ORDER BY SUM(ORDER_QTY) DESC) AS RNUM
                     , ITEM_CD, ITEM_NM, SUM(ORDER_QTY) AS ORDER_QTY
                  FROM(SELECT M2.ITEM_CD, M2.ITEM_NM, SUM(M2.ORDER_QTY) AS ORDER_QTY
                        FROM LO_OUT_M M1 JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
                       WHERE M1.OUTBOUND_DATE BETWEEN '2019-06-01' AND '2019-06-30'
                         AND M2.ITEM_NM LIKE '%참치%'
                       GROUP BY M2.ITEM_CD, M2.ITEM_NM
                       ORDER BY ORDER_QTY DESC
                      )
                 GROUP BY ITEM_CD, ITEM_NM 
                )
        )
 GROUP BY RNUM, ITEM_CD, ITEM_NM
;

