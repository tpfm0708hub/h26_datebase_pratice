                  M2.ORDER_QTY END) AS WEEKDAY_OUTPUT
     , SUM(CASE WHEN TO_CHAR(M1.OUTBOUND_DATE, 'D') IN ('1', '7') THEN
                  M2.ORDER_QTY END) AS WEEKEND_OUTPUT
