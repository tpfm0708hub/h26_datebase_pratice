     , TRUNC(SUM_QTY / QTY_IN_BOX) AS BOX_CNT
     , MOD(SUM_QTY, QTY_IN_BOX)    AS PCS_CNT
  FROM (SELECT M1.BRAND_CD, M1.ITEM_CD, M1.SUM_QTY
             , (SELECT ITEM_NM
                  FROM A_ITEM M2
                 WHERE M2.BRAND_CD = M1.BRAND_CD
                   AND M2.ITEM_CD  = M1.ITEM_CD
                ) AS ITEM_NM
             , (SELECT QTY_IN_BOX
                  FROM A_ITEM M3
                 WHERE M3.BRAND_CD = M1.BRAND_CD
                   AND M3.ITEM_CD  = M1.ITEM_CD
                ) AS QTY_IN_BOX
          FROM (SELECT BRAND_CD, ITEM_CD
                     , SUM(ORDER_QTY) AS SUM_QTY
                  FROM A_OUT_D
                 GROUP BY BRAND_CD, ITEM_CD
                ) M1
        )
;

