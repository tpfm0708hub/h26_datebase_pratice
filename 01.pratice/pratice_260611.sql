             , M4.ITEM_NM, M4.QTY_IN_BOX
          FROM (SELECT M1.BRAND_CD, M2.ITEM_CD
                     , SUM(M2.ORDER_QTY) AS SUM_QTY
                  FROM A_OUT_M M1 JOIN A_OUT_D M2 ON  M2.BRAND_CD   = M1.BRAND_CD
