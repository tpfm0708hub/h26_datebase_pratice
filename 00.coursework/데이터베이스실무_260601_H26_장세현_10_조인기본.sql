  FROM (
        SELECT M1.INVOICE_NO, M1.ORDER_NM, SUM(M2.ORDER_QTY) AS SUM_QTY
          FROM A_OUT_M M1
               JOIN A_OUT_D M2 ON  M2.BRAND_CD   = M1.BRAND_CD
                               AND M2.INVOICE_NO = M1.INVOICE_NO
         WHERE M1.BRAND_CD = '1001'
           AND M1.OUTBOUND_DATE BETWEEN TO_DATE('2023-01-04', 'YYYY-MM-DD')
                                    AND TO_DATE('2023-01-05', 'YYYY-MM-DD')
         GROUP BY M1.INVOICE_NO, M1.ORDER_NM
         ORDER BY SUM_QTY DESC
        )
 WHERE ROWNUM = 1
;

