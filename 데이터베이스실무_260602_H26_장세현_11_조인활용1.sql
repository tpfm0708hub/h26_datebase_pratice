SELECT *
  FROM (
        SELECT M1.INVOICE_NO, M1.OUTBOUND_NO, M1.OUTBOUND_BATCH, M2.ITEM_CD, M2.ITEM_NM, SUM(M2.ORDER_QTY) AS ORDER_QTY
          FROM LO_OUT_M M1  
               JOIN LO_OUT_D M2  ON M2.INVOICE_NO = M1.INVOICE_NO
                                AND M2.ITEM_NM LIKE    '%골뱅이%'
         WHERE M1.OUTBOUND_DATE BETWEEN '2019-09-03' AND '2019-09-10'
         GROUP BY M1.INVOICE_NO, M1.OUTBOUND_NO, M1.OUTBOUND_BATCH, M2.ITEM_CD, M2.ITEM_NM
         ORDER BY ORDER_QTY DESC, M1.INVOICE_NO
        )
 WHERE ROWNUM <= 5
