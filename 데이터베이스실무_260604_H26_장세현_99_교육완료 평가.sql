--문제⑤
WITH BASE_01 AS (
SELECT M1.OUTBOUND_DATE
     , TO_CHAR(M1.OUTBOUND_DATE, 'DY') AS DY
     , SUM(M2.ORDER_QTY) AS SUM_QTY
  FROM LO_OUT_M M1
       JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
 WHERE M1.OUTBOUND_DATE BETWEEN ADD_MONTHS(TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD'), -1)
                            AND TO_DATE(:OUTBOUND_DATE, 'YYYY-MM-DD') 
 GROUP BY M1.OUTBOUND_DATE
),
BASE_02 AS (
SELECT *
  FROM BASE_01
 WHERE SUM_QTY >= 170000
),
BASE_03 AS (
SELECT TO_CHAR(OUTBOUND_DATE, 'DY') AS DY
     , ROUND(AVG(SUM_QTY)) AS AVG_QTY
  FROM BASE_01
 GROUP BY TO_CHAR(OUTBOUND_DATE, 'DY')
)
SELECT R1.OUTBOUND_DATE, R1.DY, R1.SUM_QTY
    , CASE WHEN R1.SUM_QTY > R2.AVG_QTY THEN 'High' ELSE 'Low' END AS GRADE
    , R2.AVG_QTY
 FROM BASE_02 R1
     JOIN BASE_03 R2 ON R1.DY = R2.DY
 ORDER BY R1.OUTBOUND_DATE
;

