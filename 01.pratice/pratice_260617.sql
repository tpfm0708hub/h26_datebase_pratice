     , TO_CHAR(OUTBOUND_DATE, 'DY')         AS DY
     , SUM_QTY
     , CASE WHEN SUM_QTY > ROUND(AVG(SUM_QTY) OVER(PARTITION BY TO_CHAR(OUTBOUND_DATE, 'DY'))) THEN 'High'
